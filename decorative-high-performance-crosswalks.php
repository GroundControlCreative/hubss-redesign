						<h2 class="pt-0">It was twenty five years ago we started working on a better way</h2>
						<p>
							Years after the advent of stamped concrete a couple of folks in Kelowna, BC clued into the logical notion that it might be possible to stamp asphalt. The result was <a class="orange-color" href="<?php echo $base_url.'streetprint-genuine-stamped-asphalt.php'?>"> StreetPrint Pavement Texturing – Genuine stamped Asphalt; </a> taking a wire-net template, stamping it into already installed or reheated hot-mix asphalt and then applying a specialized coating to the surface and voila! All the benefits of an asphalt substrate and now attractive also!
						</p>
						<img src="assets/img/solutions/crosswalks/Unusual Rainbow crosswalk design in Granby Quebec.jpg" class="img-fluid pb-40">
						<p>
							Well the Public Works folks and Civil Engineers loved the idea of stamped asphalt for their enhanced crosswalks. 
							The logic was unassailable, no introduction of foreign materials into the monolithic road surface meant a host of familiar problems with decorative crosswalk introduction into roadways could be eliminated not the least of which was unwanted maintenance costs and the subsequent associated costs and headaches. The problem with StreetPrint for crosswalks is well, the coating wore off. Often sooner rather than later. The dimensional stamped pattern in the asphalt would last but try as they might, and boy did they try, the coating couldn’t be made to last long enough to satisfy expectations in these demanding application. Don’t get it wrong the coating technology – <a  href="<?php echo $base_url.'streetbond-150.php'?>" class="orange-color"> StreetBond150 – Coatings for Asphalt </a> – has come a long way and is a phenomenal product for colourization of asphalt surfaces. However the challenge of making attractive crosswalks in busy intersections last is truly daunting. But not impossible!
						</p>
						<h3>Twelve years ago decorative stamped asphalt crosswalks that work finally hit the road</h3>
						<p>
							The desire for decorative high performance crosswalks within urban streetscape redevelopments coupled with the logic of treating the existing road surface rather than messily and expensively introducing different material such as pavers into it, drove the developers of stamped decorative asphalt to new heights in their quest to build a better crosswalk. That’s when they started developing a specialized preformed thermoplastic.
						</p>
						<img src="assets/img/Durable decorative crosswalks in Victoria BC.jpg" class="img-fluid pb-40">
						<p>
							To not get any more boring with details than we already have, what they ended up with was no ordinary road marking kind of preformed thermoplastic but one much thicker with proprietary fortified properties. It is a material built very much like asphalt actually; graded aggregate with  specialized thermoplastic binders rather than heavy oils. In a nutshell the road surface is prepared, preformed thermoplastic panels laid and the surface, melted onto it and finally stamped to give it texture and a most attractive finish. So the point to this whole story is that through a lot of work by smart and inventive people over a long period of time the pinnacle of decorative crosswalk technology was achieved. A crosswalk that is both beautiful and incredibly durable even in the most demanding of Canadian streets, form finally met function.
						</p>
