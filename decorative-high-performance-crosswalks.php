							Ever wonder about the etymology of that saying? Anyway,<a href="<?php echo $base_url.'trafficpatterns-xd.php'?>" class="orange-color"> TrafficPatternsXD </a> continues to become the decorative high performance crosswalks of choice for towns and cities throughout North America. Those that have tried it are impressed by its performance and the true tribute to the product is they continue to put more of it down in their crosswalks season after season.
						</p>
